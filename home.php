        <h1>INK-12</h1>
        <h2>
          <span class="special">Teaching</span> and <span class="special">Learning</span> using <em>Interactive Ink Inscriptions</em> in <em>K-12</em>
        </h2>
        <p>
          The NSF-sponsored INK-12 project is investigating three educational technology questions:
        </p>
        <ol>
          <li>How can technology that allows young students to both draw and use representational tools support learning mathematics, in particular, the concepts of multiplication and division?</li>
          <li>How can technology that enables a teacher to view and share student work with her class support studentsâ€™ learning?</li>
          <li>What role can machine "understanding" of student work play in facilitating the teaching and learning of multiplication and division?</li>
        </ol>
        <p>
          To investigate these questions, we have designed, implemented, and tested software tools for our tablet-based classroom interaction system called Classroom Learning Partner (CLP). Using CLP, in conjunction with curricula such as TERC's Investigations in Number, Data, and Space, students create representations of mathematical objects and operations, CLP uses automatic analysis routines to interpret the representations, and students and teacher share the student work. CLP thus supports a create, interpret, share model of interaction.
        </p>
