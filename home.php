          <h3>
           Pen-Based Wireless Interaction
          </h3>
          <p>
            Students use a tablet pen to create and manipulate mathematical representations and wirelessly send the representations to their teacher. The complete history of students' interaction with the computer is saved along with the final representation and is thus available for analysis by teacher and researchers. CLP performs automatic analysis and sorting of students' work to help teachers choose appropriate examples for class discussion and to help identify students who may be struggling with particular concepts.
          </p>
